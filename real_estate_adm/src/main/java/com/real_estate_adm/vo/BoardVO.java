package com.real_estate_adm.vo;

public class BoardVO {
	private int idx;
	private String title;
	private String img_src;
	private String img_name;
	private String region_do_val;
	private String region_si_val;
	private String region_gu_val;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getImg_src() {
		return img_src;
	}
	public void setImg_src(String img_src) {
		this.img_src = img_src;
	}
	public String getImg_name() {
		return img_name;
	}
	public void setImg_name(String img_name) {
		this.img_name = img_name;
	}
	public String getRegion_do_val() {
		return region_do_val;
	}
	public void setRegion_do_val(String region_do_val) {
		this.region_do_val = region_do_val;
	}
	public String getRegion_si_val() {
		return region_si_val;
	}
	public void setRegion_si_val(String region_si_val) {
		this.region_si_val = region_si_val;
	}
	public String getRegion_gu_val() {
		return region_gu_val;
	}
	public void setRegion_gu_val(String region_gu_val) {
		this.region_gu_val = region_gu_val;
	}
	
	
}
